int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb == 1)
		return (1);
	if (nb < 1)
		return (0);
	while (i++ < nb)
	{
		if ((i * i) == nb)
			return (i);
	}
	return (0);
}
